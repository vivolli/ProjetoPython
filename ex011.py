largura = float(input('Digite a largura da parede: '))
altura = float(input('Digite a altura da parede: '))
area = (largura * altura)
print(f'Sua parede tem dimensão {altura} x {largura}, e a sua área é de {area}m²')
tinta = area/2
print(f'Com uma área de {area}m², vôce ira precisar de {tinta}L de tinta')
